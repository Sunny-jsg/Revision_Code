package com.ashokit.in;

public class AssignmentOperator {
	public static void main(String[] args) {
		int a = 10;
		int b = 12;
		int c = 0;
		c = a + b;
		System.out.println(c);
		c += a;
		System.out.println(c);
		c -= a;
		System.out.println(c);
		c *= a;
		System.out.println(c);
		c /= a;
		System.out.println(c);
		c %= a;
		System.out.println(c);
	}

}
